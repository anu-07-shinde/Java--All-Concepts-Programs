package IterativeStatement;

public class WhileLoopEvenNO {
	public static void main(String[] args) {
		
		int i = 0;
		
		while(i<=10)
		{
			if(i % 2 == 0)
			{
			System.out.println(i);
		}
		
		i++;
	}
		
		System.out.println("");

		// Reverse print even number between 1 to 10;
		
		int j = 10;
		
		while(j >= 0)
		{
			if(j % 2 == 0)
			{
				System.out.println(j);
			}
			j--;
		}
}
	
	
	
}





