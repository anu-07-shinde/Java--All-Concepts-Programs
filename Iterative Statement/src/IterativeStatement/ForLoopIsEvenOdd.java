package IterativeStatement;

public class ForLoopIsEvenOdd {

	public static void main(String[] args) {

		for( int i = 1; i <= 10; i++ )
		{
			if(i % 2 == 0)
			{
				System.out.println(i + "The Number is Even Number");
			
			}else {
				
				System.out.println(i + "The Number is Odd Number");
			}

	}

}
}
