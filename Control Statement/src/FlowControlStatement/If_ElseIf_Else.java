package FlowControlStatement;

public class If_ElseIf_Else {

	public static void main(String[] args) {

		int marks = 55;
		
		if(marks > 90)
		{
			System.out.println("You will get Bike...");
			
		}else if(marks > 80)
		{
			System.out.println("You will get Laptop...");
			
		}else if(marks > 70)
		{
			System.out.println("You will get Mobile");
			
		}else {
			
			System.out.println("You will get sweet....");
		}
		

	}

}
