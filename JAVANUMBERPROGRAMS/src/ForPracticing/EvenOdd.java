package ForPracticing;

public class EvenOdd {
	
	public static void main(String[] args) {
		
		System.out.println("Even Num: ");
	for(int i=0; i<100; i++)
	{
		if(i%2==0)
		{
			System.out.print(i+" ");
		}
	}
	System.out.println();
	System.out.println("Positive Num: ");
	for(int i=0; i<100; i++)
	{
		if(i%2 != 0)
		{
			System.out.print(i+" ");
		}
	}
		
	}

}
