package Operators;

public class ArithmeticOperator {

	public static void main(String[] args) {
		// + Operator
		
		int a = 100;
		int b = 20;
		int c = a + b;
		
		System.out.println("a= "+ a + " " + "b= " + b + " " + "Addition is: " + c);
		System.out.println();
		
		
		
		// + Operator
		
        int d = a - b;
		
		System.out.println("a= "+ a + " " + "b= " + b + " " + "Substraction is: " + d);
		System.out.println();
		
		
		// * Operator
		
		int x = a * b;
		
		System.out.println("a= "+ a + " " + "b= " + b + " " + "Multiplication is: " + x);
		System.out.println();
	
		
		// / Operator
		
		int y = a / b;
		
		System.out.println("a= "+ a + " " + "b= " + b + " " + "Division is: " + y);
		System.out.println();
		
		
		// % Operator
		
		int z = a % b;
		System.out.println("a= "+ a + " " + "b= " + b + " " + "Modulus is: " + z);
		
	}

}
