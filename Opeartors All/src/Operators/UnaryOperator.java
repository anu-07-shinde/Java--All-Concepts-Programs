package Operators;

public class UnaryOperator {

	public static void main(String[] args) {
		
		// Increment
		
		//Postfix Operator
		
	
		
		int x = 10;
		
		System.out.println("Prefix Operator: ");
		
		System.out.println("X: " + x);
		System.out.println("X: " + x++);
		System.out.println("X: " + x);
		
		System.out.println();
		
		// Prefix Operator
		
		int y = 20;
		
		System.out.println("Postfix Operator: ");
		
		System.out.println("Y: " + y);
		System.out.println("Y: " + ++y);
		System.out.println("Y: " + y);
		
		
		
	// Decrement
		
		//Postfix Operator
		
	
		
		System.out.println("Prefix Operator: ");
		
		System.out.println("X: " + x);
		System.out.println("X: " + x--);
		System.out.println("X: " + x);
		
		System.out.println();
		
		// Prefix Operator
		
		System.out.println("Postfix Operator: ");
		
		System.out.println("Y: " + y);
		System.out.println("Y: " + --y);
		System.out.println("Y: " + y);
	}
	
	

}
